package Section06A_Composition.Codelab030_Composition;

public class Resolution {

    private int width;
    private int height;

    // Constructor
    public Resolution(int width, int height) {
        this.width = width;
        this.height = height;
    }

    public int getWidth() {
        return width;
    }

    public int getHeight() {
        return height;
    }

}
